public class area {

    static int addNumbers(int num1, int num2) {
        return num1 + num2;
    }

    public static void main(String[] args) {
        int number1 = 10;
        int number2 = 20;

        int result = addNumbers(number1, number2);

        System.out.println("The sum is: " + result);
    }
}
